#include <linux/input-event-codes.h>

#include "input-codes.h"

const char* code_to_char(unsigned short code)
{
    switch (code) {
        case KEY_1:                    return "1";
        case KEY_2:                    return "2";
        case KEY_3:                    return "3";
        case KEY_4:                    return "4";
        case KEY_5:                    return "5";
        case KEY_6:                    return "6";
        case KEY_7:                    return "7";
        case KEY_8:                    return "8";
        case KEY_9:                    return "9";
        case KEY_0:                    return "0";
        case KEY_NUMERIC_0:            return "[N_0]";
        case KEY_NUMERIC_1:            return "[N_1]";
        case KEY_NUMERIC_2:            return "[N_2]";
        case KEY_NUMERIC_3:            return "[N_3]";
        case KEY_NUMERIC_4:            return "[N_4]";
        case KEY_NUMERIC_5:            return "[N_5]";
        case KEY_NUMERIC_6:            return "[N_6]";
        case KEY_NUMERIC_7:            return "[N_7]";
        case KEY_NUMERIC_8:            return "[N_8]";
        case KEY_NUMERIC_9:            return "[N_9]";
        case KEY_MINUS:                return "-";
        case KEY_EQUAL:                return "=";
        case KEY_BACKSPACE:            return "[BKSP]";
        case KEY_DELETE:               return "[DEL]";
        case KEY_A:                    return "A";
        case KEY_B:                    return "B";
        case KEY_C:                    return "C";
        case KEY_D:                    return "D";
        case KEY_E:                    return "E";
        case KEY_F:                    return "F";
        case KEY_G:                    return "G";
        case KEY_H:                    return "H";
        case KEY_I:                    return "I";
        case KEY_J:                    return "J";
        case KEY_K:                    return "K";
        case KEY_L:                    return "L";
        case KEY_M:                    return "M";
        case KEY_N:                    return "N";
        case KEY_O:                    return "O";
        case KEY_P:                    return "P";
        case KEY_Q:                    return "Q";
        case KEY_R:                    return "R";
        case KEY_S:                    return "S";
        case KEY_T:                    return "T";
        case KEY_U:                    return "U";
        case KEY_V:                    return "V";
        case KEY_W:                    return "W";
        case KEY_X:                    return "X";
        case KEY_Y:                    return "Y";
        case KEY_Z:                    return "Z";
        case KEY_COMMA:                return ",";
        case KEY_DOT:                  return ".";
        case KEY_SEMICOLON:            return ";";
        case KEY_RIGHTSHIFT:           return "[RSFT]";
        case KEY_LEFTSHIFT:            return "[LSFT]";
        case KEY_APOSTROPHE:           return "'";
        case KEY_BACKSLASH:            return "\\";
        case KEY_SLASH:                return "/";
        case KEY_CAPSLOCK:             return "[CAPSL]";
        case KEY_SPACE:                return " ";
        case KEY_LEFTALT:              return "[LALT]";
        case KEY_RIGHTALT:             return "[RALT]";
        case KEY_F1:                   return "[F1]";
        case KEY_F2:                   return "[F2]";
        case KEY_F3:                   return "[F3]";
        case KEY_F4:                   return "[F4]";
        case KEY_F5:                   return "[F5]";
        case KEY_F6:                   return "[F6]";
        case KEY_F7:                   return "[F7]";
        case KEY_F8:                   return "[F8]";
        case KEY_F9:                   return "[F9]";
        case KEY_F10:                  return "[F10]";
        case KEY_F11:                  return "[F11]";
        case KEY_F12:                  return "[F12]";
        case KEY_F13:                  return "[F13]";
        case KEY_F14:                  return "[F14]";
        case KEY_F15:                  return "[F15]";
        case KEY_F16:                  return "[F16]";
        case KEY_F17:                  return "[F17]";
        case KEY_F18:                  return "[F18]";
        case KEY_F19:                  return "[F19]";
        case KEY_F20:                  return "[F20]";
        case KEY_F21:                  return "[F21]";
        case KEY_F22:                  return "[F22]";
        case KEY_F23:                  return "[F23]";
        case KEY_F24:                  return "[F24]";
        case KEY_FN:                   return "[FN]";
        case KEY_FN_F1:                return "[FN_F1]";
        case KEY_FN_F2:                return "[FN_F2]";
        case KEY_FN_F3:                return "[FN_F3]";
        case KEY_FN_F4:                return "[FN_F4]";
        case KEY_FN_F5:                return "[FN_F5]";
        case KEY_FN_F6:                return "[FN_F6]";
        case KEY_FN_F7:                return "[FN_F7]";
        case KEY_FN_F8:                return "[FN_F8]";
        case KEY_FN_F9:                return "[FN_F9]";
        case KEY_FN_F10:               return "[FN_F10]";
        case KEY_FN_F11:               return "[FN_F11]";
        case KEY_FN_F12:               return "[FN_F12]";
        case KEY_FN_1:                 return "[FN_1]";
        case KEY_FN_2:                 return "[FN_2]";
        case KEY_FN_D:                 return "[FN_D]";
        case KEY_FN_E:                 return "[FN_E]";
        case KEY_FN_F:                 return "[FN_F]";
        case KEY_FN_S:                 return "[FN_S]";
        case KEY_FN_B:                 return "[FN_B]";
        case KEY_FN_RIGHT_SHIFT:       return "[FN_RSFT]";
        case KEY_NUMLOCK:              return "[NUML]";
        case KEY_SCROLLLOCK:           return "[SCRLL]";
        case KEY_COPY:                 return "[COPY]";
        case KEY_CUT:                  return "[CUT]";
        case KEY_PASTE:                return "[PASTE]";
        case KEY_RIGHT:                return "[RIGHT]";
        case KEY_LEFT:                 return "[LEFT]";
        case KEY_UP:                   return "[UP]";
        case KEY_DOWN:                 return "[DOWN]";
        case KEY_DOLLAR:               return "[DOLLAR]";
        case KEY_EURO:                 return "[EURO]";
        case KEY_ESC:                  return "[ESC]";
        case KEY_TAB:                  return "[TAB]";
        case KEY_LEFTMETA:             return "[LMETA]";
        case KEY_RIGHTMETA:            return "[RMETA]";
        case KEY_ENTER:                return "[ENTER]";
        case KEY_LEFTCTRL:             return "[LCTRL]";
        case KEY_RIGHTCTRL:            return "[RCTRL]";
        case KEY_HOME:                 return "[HOME]";
        case KEY_END:                  return "[END]";
        case KEY_INSERT:               return "[INSERT]";
        case KEY_PAGEUP:               return "[PGUP]";
        case KEY_PAGEDOWN:             return "[PGDOWN]";
        case KEY_LEFTBRACE:            return "[";
        case KEY_RIGHTBRACE:           return "]";
        default:                       return "[_]";
    }
}
